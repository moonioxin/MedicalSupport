package com.medicalsupport.httpentity;

import java.util.List;

import com.medicalsupport.httpentity.MemberResponseRow;

public class MemberResponse {
	private int page;
	private int total;
	private List<MemberResponseRow> rows;
	public int getPage() {
		return page;
	}
	public void setPage(int page) {
		this.page = page;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public List<MemberResponseRow> getRows() {
		return rows;
	}
	public void setRows(List<MemberResponseRow> rows) {
		this.rows = rows;
	}
}
